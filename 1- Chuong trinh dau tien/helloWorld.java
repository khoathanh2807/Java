/* Chương trình sử dụng câu lệnh print / println */
/* Chương trình Java đầu tiên dùng để in chữ ra màn hình */

public class helloWorld {

    public static void main(String[] args) {
        
        //lệnh \n dùng để xuống dòng khi in kí tự ra màn hình
        System.out.print("Hello World! \n");

        //lệnh \t dùng chèn khoảng trắng khi in kí tự ra màn hình
        System.out.println("Vi du ki tu chen \tkhoang trang");

    }

}